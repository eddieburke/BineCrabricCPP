function M.count() return #M.entries end

function M.find(key, value)
  local found = {}
  for _, entry in ipairs(M.entries) do
    if entry[key] == value then
      found[#found + 1] = entry
    end
  end
  return found
end

function M.first(key, value)
  for _, entry in ipairs(M.entries) do
    if entry[key] == value then return entry end
  end
  return nil
end

function M.each(callback)
  if type(callback) ~= "function" then return 0 end
  local visited = 0
  for index, entry in ipairs(M.entries) do
    callback(entry, index)
    visited = visited + 1
  end
  return visited
end

function M.summary()
  local result = { id = M.id, count = #M.entries, groups = {} }
  for _, entry in ipairs(M.entries) do
    local group = entry.kind or entry.category or entry.holiday or entry.room or entry.feature or entry.service or entry.permission or entry.event or "default"
    result.groups[group] = (result.groups[group] or 0) + 1
  end
  return result
end

